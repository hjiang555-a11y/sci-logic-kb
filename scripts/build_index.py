#!/usr/bin/env python3
"""Auto-generate hierarchical INDEX files from YAML source of truth.

Produces:
  - ROOT/INDEX.md            — topic overview / jump page
  - ROOT/topics/<t>/INDEX.md — per-topic detailed node & relation index
  - ROOT/INDEX_metrics.md    — cross-topic metrics quick-reference
  - ROOT/INDEX_principles.md — cross-topic principles quick-reference
"""

from __future__ import annotations

import argparse
import sys
from collections import defaultdict
from datetime import datetime, timezone
from pathlib import Path
from typing import Any

import yaml

# ---------------------------------------------------------------------------
# Constants
# ---------------------------------------------------------------------------

HEADER = "<!-- AUTO-GENERATED by scripts/build_index.py — DO NOT EDIT MANUALLY -->\n\n"

TOPIC_DISPLAY_NAMES: dict[str, str] = {
    "ultrastable-laser": "Ultrastable Laser",
    "optical-frequency-combs": "Optical Frequency Combs",
    "frequency-standards": "Frequency Standards",
    "timescales": "Timescales",
    "time-frequency-transfer": "Time-Frequency Transfer",
}

TIER_ORDER = ["meta", "foundational", "domain", "engineering"]

# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------


def _md_escape(text: str) -> str:
    """Escape pipe and newline characters for markdown table cells."""
    if not isinstance(text, str):
        text = str(text)
    text = text.replace("\n", " ").replace("\r", "")
    text = text.replace("|", "\\|")
    return text.strip()


def _truncate(text: str, max_len: int = 80) -> str:
    text = _md_escape(text)
    if len(text) > max_len:
        return text[: max_len - 1] + "…"
    return text


def _relative_paper_path(yaml_path: Path, repo: Path) -> str:
    """Return e.g. 'topics/ultrastable-laser/papers/matei2017.yaml'."""
    return str(yaml_path.relative_to(repo))


def _paper_label(yaml_path: Path) -> str:
    """Return e.g. 'matei2017'."""
    return yaml_path.stem


def _get_demonstrated_value(metric: dict) -> tuple[str, str]:
    """Return (value_str, conditions_str) from a metric node."""
    dv = metric.get("demonstrated_value")
    if dv is None:
        return ("—", "—")
    if isinstance(dv, dict):
        val = dv.get("value", "—")
        cond = dv.get("conditions", "—")
    else:
        val = str(dv)
        cond = metric.get("conditions", "—")
    return (_truncate(str(val), 100), _truncate(str(cond), 80))


# ---------------------------------------------------------------------------
# YAML loading
# ---------------------------------------------------------------------------


def load_all_papers(repo: Path) -> list[dict[str, Any]]:
    """Load every topics/*/papers/*.yaml and attach file metadata."""
    results: list[dict[str, Any]] = []
    topics_dir = repo / "topics"
    if not topics_dir.is_dir():
        return results
    for yaml_path in sorted(topics_dir.glob("*/papers/*.yaml")):
        try:
            with yaml_path.open("r", encoding="utf-8") as fh:
                data = yaml.safe_load(fh)
        except Exception as exc:
            print(f"WARNING: failed to parse {yaml_path}: {exc}", file=sys.stderr)
            continue
        if not isinstance(data, dict):
            continue
        # Infer topic from directory structure
        topic = yaml_path.parent.parent.name
        data.setdefault("meta", {})
        data["meta"].setdefault("topic", topic)
        data["_path"] = yaml_path
        data["_topic"] = topic
        results.append(data)
    return results


def _as_list(section: Any) -> list[dict]:
    """Normalise a YAML section that could be a list or dict-of-dicts."""
    if section is None:
        return []
    if isinstance(section, list):
        return [item for item in section if isinstance(item, dict)]
    if isinstance(section, dict):
        out = []
        for key, val in section.items():
            if isinstance(val, dict):
                val.setdefault("id", key)
                out.append(val)
        return out
    return []


# ---------------------------------------------------------------------------
# Data aggregation
# ---------------------------------------------------------------------------


class TopicData:
    """Aggregated data for one topic."""

    def __init__(self, topic: str):
        self.topic = topic
        self.papers: list[dict] = []
        self.entities: list[tuple[dict, Path]] = []
        self.principles: list[tuple[dict, Path]] = []
        self.methods: list[tuple[dict, Path]] = []
        self.metrics: list[tuple[dict, Path]] = []
        self.relations: list[tuple[dict, Path]] = []

    @property
    def paper_count(self) -> int:
        return len(self.papers)

    @property
    def node_count(self) -> int:
        return (
            len(self.entities)
            + len(self.principles)
            + len(self.methods)
            + len(self.metrics)
        )

    @property
    def relation_count(self) -> int:
        return len(self.relations)


def aggregate(papers: list[dict]) -> dict[str, TopicData]:
    topics: dict[str, TopicData] = {}
    # Ensure all topic dirs are represented even if empty
    for p in papers:
        topic = p["_topic"]
        if topic not in topics:
            topics[topic] = TopicData(topic)
        td = topics[topic]
        td.papers.append(p)
        path = p["_path"]
        for ent in _as_list(p.get("entities")):
            td.entities.append((ent, path))
        for pri in _as_list(p.get("principles")):
            td.principles.append((pri, path))
        for meth in _as_list(p.get("methods")):
            td.methods.append((meth, path))
        for met in _as_list(p.get("metrics")):
            td.metrics.append((met, path))
        for rel in _as_list(p.get("relations")):
            td.relations.append((rel, path))
    return topics


def discover_topic_dirs(repo: Path) -> list[str]:
    """Return all topic directory names, even those with 0 papers."""
    topics_dir = repo / "topics"
    if not topics_dir.is_dir():
        return []
    return sorted(
        d.name
        for d in topics_dir.iterdir()
        if d.is_dir() and (d / "papers").is_dir()
    )


